static void Gauge_DrawRing(void)
{
    FRONT_COLOR = DARKBLUE;
    LCD_Draw_Circle(160, 260, 120);
    LCD_Draw_Circle(160, 260, 121);
    LCD_Draw_Circle(160, 260, 122);

    FRONT_COLOR = LIGHTGRAY;
    LCD_Draw_Circle(160, 260, 108);
    LCD_Draw_Circle(160, 260, 109);
}

static void Gauge_DrawTicks(void)
{
    u8 i;

    for(i = 0; i < sizeof(gauge_minor_ticks) / sizeof(gauge_minor_ticks[0]); i++)
    {
        LCD_DrawLine_Color(gauge_minor_ticks[i].x1, gauge_minor_ticks[i].y1,
                           gauge_minor_ticks[i].x2, gauge_minor_ticks[i].y2, GRAYBLUE);
    }

    for(i = 0; i < sizeof(gauge_major_ticks) / sizeof(gauge_major_ticks[0]); i++)
    {
        LCD_DrawLine_Color(gauge_major_ticks[i].x1, gauge_major_ticks[i].y1,
                           gauge_major_ticks[i].x2, gauge_major_ticks[i].y2, RED);
    }
}

static void Gauge_DrawLabels(void)
{
    u8 i;
    u16 old_front = FRONT_COLOR;
    u16 old_back = BACK_COLOR;

    FRONT_COLOR = BLACK;
    BACK_COLOR = WHITE;

    for(i = 0; i < sizeof(gauge_labels) / sizeof(gauge_labels[0]); i++)
    {
        LCD_ShowNum(gauge_labels[i].x, gauge_labels[i].y, gauge_labels[i].value, 3, 12);
    }

    LCD_ShowString(122, 332, 80, 24, 16, (u8 *)"km/h");
    LCD_ShowString(116, 356, 96, 24, 12, (u8 *)"STM32 DASH");

    FRONT_COLOR = old_front;
    BACK_COLOR = old_back;
}

static void Gauge_DrawPointer(u8 index)
{
    const GaugeLine *pointer;

    if(index >= (sizeof(gauge_pointer_lines) / sizeof(gauge_pointer_lines[0])))
    {
        return;
    }

    pointer = &gauge_pointer_lines[index];
    LCD_DrawLine_Color(pointer->x1, pointer->y1, pointer->x2, pointer->y2, RED);
    LCD_DrawLine_Color(pointer->x1 + 1, pointer->y1, pointer->x2 + 1, pointer->y2, BRRED);
    LCD_DrawLine_Color(pointer->x1, pointer->y1 + 1, pointer->x2, pointer->y2 + 1, BRRED);

    LCD_Fill(154, 254, 166, 266, DARKBLUE);
    LCD_Fill(157, 257, 163, 263, YELLOW);
}

static void Gauge_ShowValue(u16 value)
{
    u16 old_front = FRONT_COLOR;
    u16 old_back = BACK_COLOR;

    FRONT_COLOR = YELLOW;
    BACK_COLOR = BLACK;
    LCD_Fill(116, 286, 204, 322, BLACK);
    LCD_ShowNum(124, 290, value, 3, 24);

    FRONT_COLOR = old_front;
    BACK_COLOR = old_back;
}

static void Gauge_Render(u8 index)
{
    LCD_Clear(WHITE);
    LCD_Fill(20, 20, 299, 399, LGRAY);
    LCD_Fill(24, 24, 295, 395, WHITE);
    Gauge_DrawRing();
    Gauge_DrawTicks();
    Gauge_DrawLabels();
    Gauge_DrawPointer(index);
    Gauge_ShowValue(index * 10);
}

static void Gauge_RunDemo(void)
{
    u8 index;

    for(index = 0; index < 25; index++)
    {
        Gauge_Render(index);
        delay_ms(90);
    }

    for(index = 23; index > 0; index--)
    {
        Gauge_Render(index);
        delay_ms(60);
    }

    Gauge_Render(0);
}

int main()
{
    u8 blink = 0;

    HAL_Init();
    SystemClock_Init(RCC_PLL_MUL9);
    SysTick_Init(72);
    USART1_Init(115200);
    LED_Init();
    TFTLCD_Init();

    Gauge_RunDemo();

    while(1)
    {
        blink++;
        if(blink % 20 == 0)
        {
            LED1 = !LED1;
        }

        delay_ms(10);
    }
}